/**
 * Render file as bytes table
 * @param filePath
 */
function run(filePath) {
  icon();
  return


  const stream = fs.createReadStream(filePath);

  stream.on('end', () => {
    process.stdout.write('\n');
    console.log(`Done!`);
  });

  stream.on('open', () => {
    console.log(`Reading ${filePath}`);
  });

  stream.on('error', (err) => {
    console.error(err);
  });

  stream.on('data', (data) => {
    // return icon();
    // renderBytesTable(data);
    // return;


    /** @type {boolean} */
    let isCountZeros = false;
    /** @type {boolean} */
    let isCountFulls = false;
    /** @type {number} */
    let count = 0;

    let output = `const raw = Buffer.allocUnsafe(${data.length});\n`;

    const wordsCount = (Math.floor(data.length / 8));
    const fullWord = (1n << 64n) - 1n;
    let offset = 0;

    for (let i = 0; i < wordsCount; i++) {
      offset = i * 8;
      const word = data.readBigUInt64BE(offset);

      if (word === 0n) {
        isCountZeros = true;
        count += 1;
        continue;
      }
      if (word !== 0n && isCountZeros) {
        const from = offset - (count * 8);
        const to = offset;
        output += `raw.fill(0, 0x${from.toString(16)}, 0x${to.toString(16)});\n`

        isCountZeros = false;
        count = 0;
      }

      if (word === fullWord) {
        isCountFulls = true;
        count += 1;
        continue;
      }
      if (word !== fullWord && isCountFulls) {
        const from = offset - (count * 8);
        const to = offset;
        output += `raw.fill(255, 0x${from.toString(16)}, 0x${to.toString(16)});\n`

        isCountFulls = false;
        count = 0;
      }

      output += `raw.writeBigUint64BE(${word}n, ${offset});\n`;
    }

    if (isCountZeros) {
      const from = offset - (count * 8) + 8;
      const to = data.length;
      output += `raw.fill(0, 0x${from.toString(16)}, 0x${to.toString(16)});\n`
    }

    console.log(output);
  });
}

